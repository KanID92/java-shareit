DROP TABLE IF EXISTS COMMENTS CASCADE;
DROP TABLE IF EXISTS REQUESTS CASCADE;
DROP TABLE IF EXISTS BOOKINGS CASCADE;
DROP TABLE IF EXISTS USERS CASCADE;
DROP TABLE IF EXISTS ITEMS CASCADE;

CREATE TABLE IF NOT EXISTS USERS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME    VARCHAR(50)                             NOT NULL,
    EMAIL   VARCHAR(512)                            NOT NULL,
    CONSTRAINT USERS_PK
        PRIMARY KEY (ID),
    CONSTRAINT USER_EMAIL_UNIQUE
        UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS REQUESTS
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    DESCRIPTION  VARCHAR(500)                            NOT NULL,
    REQUESTOR_ID BIGINT                                  NOT NULL,
    CONSTRAINT REQUESTS_PK
        PRIMARY KEY (ID),
    CONSTRAINT REQUESTS_REQUESTOR_ID_FK_USERS_USER_ID
        FOREIGN KEY (REQUESTOR_ID) REFERENCES USERS (ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS ITEMS
(
    ID      BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME         VARCHAR(50)                             NOT NULL,
    DESCRIPTION  VARCHAR(255)                            NOT NULL,
    IS_AVAILABLE BOOLEAN                                 NOT NULL,
    OWNER_ID     BIGINT                                  NOT NULL,
    REQUEST_ID   BIGINT,
    CONSTRAINT ITEMS_PK
        PRIMARY KEY (ID),
    CONSTRAINT ITEMS_OWNER_ID_FK_USERS_USER_ID
        FOREIGN KEY (OWNER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT ITEMS_REQUEST_ID_FK_ITEMS_REQUEST_ID
        FOREIGN KEY (REQUEST_ID) REFERENCES REQUESTS (ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS BOOKINGS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    BOOKER_ID  BIGINT                                  NOT NULL,
    ITEM_ID    BIGINT                                  NOT NULL,
    START      TIMESTAMP                               NOT NULL,
    FINISH     TIMESTAMP                               NOT NULL,
    STATUS     VARCHAR(15)                             NOT NULL,
    CONSTRAINT BOOKINGS_PK
        PRIMARY KEY (ID),
    CONSTRAINT BOOKINGS_BOOKER_ID_FK_USERS_USER_ID
        FOREIGN KEY (BOOKER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT BOOKINGS_ITEM_ID_FK_ITEMS_ITEM_ID
        FOREIGN KEY (ITEM_ID) REFERENCES ITEMS (ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS COMMENTS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    TEXT       VARCHAR(500)                            NOT NULL,
    ITEM_ID    BIGINT                                  NOT NULL,
    AUTHOR_ID  BIGINT                                  NOT NULL,
    CREATED    TIMESTAMP                               NOT NULL,
    CONSTRAINT COMMENTS_PK
        PRIMARY KEY (ID),
    CONSTRAINT COMMENTS_ITEM_ID_FK_ITEMS_ITEM_ID
        FOREIGN KEY (ITEM_ID) REFERENCES ITEMS (ID) ON DELETE CASCADE,
    CONSTRAINT COMMENTS_AUTHOR_ID_FK_USERS_USER_ID
        FOREIGN KEY (AUTHOR_ID) REFERENCES USERS (ID) ON DELETE CASCADE
)